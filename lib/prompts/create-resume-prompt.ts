import { ScoreCategory } from '../config/resume-analysis-config';
import { generateBaseSystemPrompt } from './base-system-prompt';

export function generateCreateResumeSystemPrompt(categories: ScoreCategory[]): string {
    const basePrompt = generateBaseSystemPrompt(categories);
    
    const createResumeSpecificInstructions = `

### 🎯 從零打造履歷 - 專用分析指引

**服務目標**：基於零散的作品材料、專案報告、技能證明等，挖掘候選人的潛在能力，協助打造完整的專業履歷。

#### 📋 材料類型識別與處理策略

**1. 作品截圖與展示材料**
- **網站/應用程式截圖**：分析UI/UX設計能力、技術實現複雜度、用戶體驗考量
- **程式碼片段**：評估程式設計風格、技術熟練度、最佳實務應用
- **系統架構圖**：識別系統設計能力、技術選型思維、擴展性考量

**2. 專案報告與文件**
- **技術文件**：評估技術深度、文件撰寫能力、專業表達水準
- **專案企劃書**：分析專案管理能力、需求分析能力、商業思維
- **測試報告**：識別品質保證意識、測試策略、問題解決能力

**3. 學習成果與證明**
- **課程作業**：評估學習能力、技術基礎、實作能力
- **認證證書**：驗證專業技能、持續學習意願、行業認知
- **競賽作品**：分析創新能力、解決問題能力、團隊協作

#### 👤 個人基本資料提取重點

**從零散材料中捕捉個人資訊**：
- **身份識別**：從文件署名、作者資訊、簡介等處提取姓名
- **專業定位**：從專案角色、技術專長、學習領域推斷適合的專業頭銜
- **聯絡方式**：收集所有可見的聯絡資訊（郵箱、電話、社群帳號等）
- **個人簡介**：基於作品和專案表現，提煉候選人的核心優勢與特色
- **線上展示**：識別GitHub、LinkedIn、個人網站、作品集等數位足跡

**資訊整合原則**：
- **完整性優先**：盡可能提取所有可見的個人資訊
- **一致性檢查**：確保不同文件中的個人資訊保持一致
- **缺失標註**：對於無法從材料中獲得的基本資訊，在missing_content中明確標註
- **專業化包裝**：將零散的個人資訊整合成專業的履歷檔案格式

#### 🔍 潛能評估框架

**技術潛能指標**：
- **學習軌跡分析**：從基礎到進階的技術成長路徑
- **技術廣度評估**：涉及的技術領域範圍與深度
- **創新思維展現**：解決問題的創新方法與獨特見解
- **實作能力驗證**：理論知識轉化為實際應用的能力

**專業素養指標**：
- **文件品質**：技術文件的完整性、清晰度、專業性
- **設計思維**：用戶體驗考量、介面設計美感、功能性平衡
- **品質意識**：測試覆蓋率、錯誤處理、性能優化考量
- **協作能力**：團隊專案中的角色定位與貢獻度

#### 📊 評分調整原則

**考量因素**：
1. **材料完整度**：相對於傳統履歷，零散材料可能缺乏完整的工作經驗描述
2. **潛能權重**：更重視技術學習能力、創新思維、成長潛力
3. **實作證明**：通過作品和專案驗證實際能力，而非僅憑工作經歷
4. **發展軌跡**：關注學習成長曲線，預測未來發展潛力

**評分寬容度**：
- **工作經驗**：對於缺乏正式工作經驗的情況給予合理寬容
- **專案規模**：重視專案的技術含量和創新性，而非僅看規模
- **教育背景**：更關注實際技能掌握程度，而非學歷等級
- **成果展示**：重視作品品質和技術深度，認可自主學習成果

#### 🎯 履歷建構建議

**必要補強項目**：
1. **個人資料完整化**：聯絡方式、專業頭銜、個人簡介
2. **技能矩陣建立**：基於作品分析，建立完整的技術技能清單
3. **專案經驗整理**：將零散作品整理成結構化的專案經驗
4. **教育背景補充**：相關學習經歷、線上課程、自學成果
5. **成就亮點提煉**：從作品中提煉可量化的成就和亮點

**履歷結構建議**：
個人資料 → 專業技能 → 專案作品 → 教育背景 → 額外成就

#### 🔮 潛力預測與建議

**成長路徑規劃**：
- **短期目標**：基於現有技能，建議適合的初級職位
- **中期發展**：識別技能缺口，提供學習建議
- **長期規劃**：預測職涯發展方向，提供專業建議

**技能發展建議**：
- **核心技能深化**：基於現有作品，建議深化的技術方向
- **橫向技能拓展**：建議學習的相關技術領域
- **軟技能提升**：溝通、協作、專案管理等職場技能

### 🎨 創意履歷打造策略

**差異化定位**：
- **技術特色突出**：識別候選人的技術特色和優勢
- **作品集整合**：將最佳作品整合成專業作品集
- **故事化表達**：將技術經歷包裝成引人入勝的職涯故事
- **視覺化呈現**：建議適合的履歷視覺化元素和排版

**個人品牌建立**：
- **專業形象定位**：基於技能和作品，建立專業形象
- **價值主張提煉**：明確候選人能為企業帶來的價值
- **差異化優勢**：識別與同級候選人的差異化競爭優勢

此專用分析框架將協助候選人將零散的學習成果和作品，轉化為具有競爭力的專業履歷，最大化展現其技術潛力和職業價值。`;

    return basePrompt + createResumeSpecificInstructions;
} 